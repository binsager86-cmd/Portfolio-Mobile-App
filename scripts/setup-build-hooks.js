#!/usr/bin/env node
/**
 * setup-build-hooks.js
 *
 * npm postinstall script that wraps the expo CLI binary to automatically
 * run SPA redirect generation after `expo export`.
 *
 * Problem:  DigitalOcean App Platform's live build command may not include
 *           our custom post-processing step, and .do/app.yaml changes don't
 *           auto-update the live app spec.
 *
 * Solution: After `npm install`, this script replaces the expo CLI symlink
 *           in node_modules/.bin/ with a wrapper that:
 *           1. Calls the real expo CLI
 *           2. If the command was `export`, also runs create-spa-redirects.js
 *
 * Only patches on Linux/macOS (CI/CD). Skips on Windows (dev machines).
 */

const fs = require('fs');
const path = require('path');

// Skip on Windows (dev environment) — only needed for Linux CI/CD
if (process.platform === 'win32') {
  console.log('[build-hooks] Windows detected, skipping CLI patch');
  process.exit(0);
}

const binDir = path.resolve(__dirname, '..', 'node_modules', '.bin');
const expoBin = path.join(binDir, 'expo');

// Resolve the real expo CLI path (follows symlink)
let realExpoPath;
try {
  realExpoPath = fs.realpathSync(expoBin);
} catch (e) {
  console.log('[build-hooks] expo binary not found in node_modules/.bin, skipping');
  process.exit(0);
}

const redirectScript = path.resolve(__dirname, 'create-spa-redirects.js');
const nodeExe = process.execPath;

// Create a shell wrapper that calls the real expo CLI, then post-processes
const wrapper = `#!/bin/sh
# Auto-generated by setup-build-hooks.js
# Wraps expo CLI to add SPA redirect post-processing after export
"${nodeExe}" "${realExpoPath}" "$@"
EXIT_CODE=$?
if [ $EXIT_CODE -eq 0 ] && echo "$@" | grep -q "export"; then
  echo ""
  echo "[build-hooks] Detected 'expo export' — generating SPA redirect files..."
  "${nodeExe}" "${redirectScript}"
fi
exit $EXIT_CODE
`;

try {
  // Remove the existing symlink
  fs.unlinkSync(expoBin);
  // Write the wrapper script
  fs.writeFileSync(expoBin, wrapper, { mode: 0o755 });
  console.log('[build-hooks] ✅ Patched expo CLI — SPA redirects will auto-generate after export');
} catch (e) {
  console.log('[build-hooks] ⚠️  Could not patch expo CLI:', e.message);
  console.log('[build-hooks] ℹ️  Run "node scripts/create-spa-redirects.js" manually after export');
}
